# Tuplas

# Como crear una tupla con string

frutas = ("manzana", "peras", "uvas")
print(frutas)

# Como crear una tupla con enteros

numeros = (5,6,4,8,9)
print(numeros)

# Como crear una tupla con diferentes tipos de datos

tuplaDatos = ("Muebles", 56, 2.6, "carros")
print(tuplaDatos)

# Determinar el valor de una dupla

print(len(tuplaDatos))

# modificar una dupla

animales = ("leon","gacela","elefante","tigre")
nuevoAnimales = animales + ("conejo", "pantera", "hipopotamo")
print(nuevoAnimales)

# convertir una lista en dupla

lista_colores = ["amarillo", "azul", "rojo"]
duplaColores = tuple(lista_colores)
print(duplaColores)

# convertir  una dupla a una lista

figuras = ("circulo", "cuadrado" ,"triangulo")
figuraslista = list(figuras)
print(figuraslista)




